package com.example.cobwebfloodreportapplication;

public class DialogueData {
	
	
	private String floodType;
	private String flowVelocity;
	private String floodDepth;
	private String note;
	
	
	public String getFloodType() {
		return floodType;
	}

	public String getFlowVelocity() {
		return flowVelocity;
	}




	public String getFloodDepth() {
		return floodDepth;
	}

	public String getNote() {
		return note;
	}

	public void setNote(String note) {
		this.note = note;
	}

	public void setFloodType(String floodType) {
		this.floodType = floodType;
	}

	public void setFlowVelocity(String flowVelocity) {
		this.flowVelocity = flowVelocity;
	}

	public void setFloodDepth(String floodDepth) {
		this.floodDepth = floodDepth;
	}




}
